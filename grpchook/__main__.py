# ---------------------------------------------------------------------------
# File writing helpers
# ---------------------------------------------------------------------------

def _write_file(destination: Path, content: str, *, overwrite: bool = False) -> bool:
    """
    Write *content* to *destination*.

    Returns True on success, False if the file already existed and *overwrite*
    is False (the existing file is left untouched in that case).
    """
    if destination.exists() and not overwrite:
        print(f"  [skip]  {destination.name} already exists — not overwritten.")
        return False

    destination.write_text(content, encoding="utf-8")
    print(f"  [ok]    {destination.name} written.")
    return True


def _copy_file(source: Path, destination: Path, *, overwrite: bool = False) -> bool:
    """
    Copy *source* to *destination*.

    Returns True on success, False if the file already existed and *overwrite*
    is False.
    """
    if destination.exists() and not overwrite:
        print(f"  [skip]  {destination.name} already exists — not overwritten.")
        return False

    shutil.copy2(source, destination)
    print(f"  [ok]    {destination.name} written.")
    return True


# ---------------------------------------------------------------------------
# Generation actions
# ---------------------------------------------------------------------------

def generate_server(output_dir: Path) -> None:
    """Write server_skeleton.py to *output_dir*."""
    _write_file(output_dir / "server_skeleton.py", _SERVER_SKELETON)


def generate_client(output_dir: Path) -> None:
    """Write client_skeleton.py to *output_dir*."""
    _write_file(output_dir / "client_skeleton.py", _CLIENT_SKELETON)


def generate_skeleton(output_dir: Path) -> None:
    """Write both server_skeleton.py and client_skeleton.py to *output_dir*."""
    generate_server(output_dir)
    generate_client(output_dir)


def generate_how_to(output_dir: Path) -> None:
    """Copy HOW_TO.md into *output_dir*."""
    try:
        source = _locate_how_to()
    except FileNotFoundError as exc:
        print(f"  [error] {exc}", file=sys.stderr)
        sys.exit(1)

    _copy_file(source, output_dir / "HOW_TO.md")


# The instructions printed after a successful --generate-interface run.
# Kept as a module-level constant so it is easy to update in one place.
_INTERFACE_INSTRUCTIONS = """
Next steps
----------
1.  Edit  message.proto  — change the Payload message to suit your needs.
    Do NOT rename or remove the Service, Message, MetaInformation, DataPoint,
    ClientProvides, or ServerProvides definitions; grpchook depends on them.

2.  Compile your proto (requires grpcio-tools):

        python -m grpc_tools.protoc -I<directory-containing-message.proto> --python_out=<same-directory> --grpc_python_out=<same-directory> --pyi_out=<same-directory> message.proto

    This produces  message_pb2.py,  message_pb2_grpc.py, and  message_pb2.pyi
    next to your  message.proto.

3.  Load your custom interface at the START of your server / client script,
    BEFORE importing BaseServer or BaseClient:

        from grpchook.custom_interface import compile_and_register
        compile_and_register(
            proto_path="path/to/your/message.proto",
            out_dir="path/to/your/proto/directory",  # where pb2 files land
        )
        # grpchook now uses your Payload definition everywhere.

    Or, if you have already compiled the proto manually:

        from grpchook.custom_interface import load_pb_modules_from_dir
        load_pb_modules_from_dir("path/to/your/proto/directory")

4.  Import your generated types for type-hinted access to the custom payload:

        import path.to.your.message_pb2 as message_pb2
        # e.g. request.payload.myCustomField
"""


def generate_interface(output_dir: Path) -> None:
    """Copy message.proto into *output_dir* and print usage instructions."""
    try:
        source = _locate_proto()
    except FileNotFoundError as exc:
        print(f"  [error] {exc}", file=sys.stderr)
        sys.exit(1)

    _copy_file(source, output_dir / "message.proto")
    print(_INTERFACE_INSTRUCTIONS)


def generate_interface_with_skeletons(output_dir: Path) -> None:
    """Copy message.proto and write server/client skeletons that use it."""
    generate_interface(output_dir)
    _write_file(output_dir / "server_skeleton.py", _SERVER_SKELETON_CUSTOM)
    _write_file(output_dir / "client_skeleton.py", _CLIENT_SKELETON_CUSTOM)


