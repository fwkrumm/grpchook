_SERVER_SKELETON = '''\
"""
Server implementation — generated by grpchook.

Replace:
  - MyServer      → your server class name
  - 50051         → your port number
  - "my_request"  → the messageName(s) your clients will send
  - "my_response" → the messageName(s) your server will push back
"""

from grpchook.baseserver import BaseServer, Peer, ServerConfig
from grpchook.tools import generate_message
import grpchook.message_pb2 as message_pb2


class MyServer(BaseServer):
    """Minimal server skeleton. Override the hooks you need."""

    def __init__(self, port: int = 50051):
        super().__init__(port=port, name="MyServer")

    # ------------------------------------------------------------------
    # Lifecycle hooks
    # ------------------------------------------------------------------

    def on_init(self):
        """Called once after __init__ completes. Set up server-side state here."""
        pass

    def on_shutdown(self):
        """Called during shutdown. Clean up resources here."""
        pass

    # ------------------------------------------------------------------
    # Client lifecycle hooks
    # ------------------------------------------------------------------

    def on_client_connect(self, request: message_pb2.Message, context) -> bool:
        """
        Called on the *first* message from a new client, before it is registered.

        request.metaInfo.clientInfo exposes:
            .uuid        — unique ID generated by the client
            .identifier  — human-readable name sent by the client (maps to client's ``name``)
            .provides    — list of messageNames the client will send
            .requires    — list of messageNames the client wants to receive

        Return True to accept the client, False to reject (triggers PERMISSION_DENIED).
        """
        return True

    def on_client_accepted(self, peer: Peer, request: message_pb2.Message):
        """
        Called after a client has been fully registered.
        peer fields are all populated at this point.
        """
        pass

    def on_client_disconnect(self, peer: Peer):
        """Called when an accepted client's stream closes."""
        pass

    # ------------------------------------------------------------------
    # Message hook
    # ------------------------------------------------------------------

    def on_receive(self, peer: Peer, request: message_pb2.Message) -> bool:
        """
        Called for every subsequent message from an accepted client.

        Return True  → the framework fans the message out to all clients whose
                       'requires' list contains this messageName.
        Return False → suppress the fan-out; handle routing yourself (e.g.
                       unicast, transform, or cache the message).

        Example — unicast a reply back to the sender only:

            response = generate_message("my_response", struct_payload={"result": 42})
            self._data_register.add_data_for_messageName(
                peer.client_id, "my_response", response,
                targetClientId=peer.client_id,   # <-- unicast
            )
            return False
        """
        return True


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    server = MyServer(port=50051)
    server.serve_forever()   # blocks until KeyboardInterrupt or server.shutdown()
'''


