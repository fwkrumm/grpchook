# Directory that contains this __main__.py (i.e. the grpchook package dir).
_PACKAGE_DIR = Path(__file__).parent


def _locate_how_to() -> Path:
    """
    Return the path to HOW_TO.md, searching in two locations:

    1. Inside the installed package directory (grpchook/HOW_TO.md).
       This is the path used when grpchook is installed via pip/pdm.
    2. Inside the assets directory one level above the package directory (assets/HOW_TO.md).
       This is the path used when running directly from the source tree
       (e.g. ``python -m grpchook`` from the project root).

    Raises FileNotFoundError if neither location contains the file.
    """
    bundled = _PACKAGE_DIR / "assets" / "HOW_TO.md"
    if bundled.exists():
        return bundled

    source_tree = _PACKAGE_DIR.parent / "assets" / "HOW_TO.md"
    if source_tree.exists():
        return source_tree

    raise FileNotFoundError(
        "HOW_TO.md not found. "
        "Re-install the grpchook package or run from the project root."
    )


def _locate_proto() -> Path:
    """
    Return the path to the bundled message.proto, searching in two locations:

    1. Inside the installed package directory (grpchook/message.proto).
    2. The interface/ directory one level above (source tree).

    Raises FileNotFoundError if neither location contains the file.
    """
    bundled = _PACKAGE_DIR / "message.proto"
    if bundled.exists():
        return bundled

    source_tree = _PACKAGE_DIR.parent / "interface" / "message.proto"
    if source_tree.exists():
        return source_tree

    raise FileNotFoundError(
        "message.proto not found. "
        "Re-install the grpchook package or run from the project root."
    )


