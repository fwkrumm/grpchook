def _build_parser() -> argparse.ArgumentParser:
    """Build and return the argument parser for the grpchook CLI."""
    parser = argparse.ArgumentParser(
        prog="python -m grpchook",
        description=(
            "grpchook scaffolding tool.\n\n"
            "Generates skeleton server/client files and the HOW_TO reference\n"
            "document into the current working directory."
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "examples:\n"
            "  python -m grpchook --generate                          # skeleton + HOW_TO\n"
            "  python -m grpchook --generate-skeletons                  # server + client only\n"
            "  python -m grpchook --generate-server                   # server only\n"
            "  python -m grpchook --generate-client                   # client only\n"
            "  python -m grpchook --generate-how-to                   # HOW_TO.md only\n"
            "  python -m grpchook --generate-interface"
            "                # message.proto + instructions\n"
            "  python -m grpchook --generate-interface-with-skeletons"
            "  # proto + matching skeletons\n"
        ),
    )

    parser.add_argument(
        "--generate",
        action="store_true",
        help="Generate server_skeleton.py, client_skeleton.py, and HOW_TO.md",
    )
    parser.add_argument(
        "--generate-skeletons",
        action="store_true",
        help="Generate server_skeleton.py and client_skeleton.py",
    )
    parser.add_argument(
        "--generate-server",
        action="store_true",
        help="Generate server_skeleton.py only",
    )
    parser.add_argument(
        "--generate-client",
        action="store_true",
        help="Generate client_skeleton.py only",
    )
    parser.add_argument(
        "--generate-how-to",
        action="store_true",
        help="Copy HOW_TO.md into the current directory",
    )
    parser.add_argument(
        "--generate-interface",
        action="store_true",
        help="Copy message.proto into the current directory and print customisation instructions",
    )
    parser.add_argument(
        "--generate-interface-with-skeletons",
        action="store_true",
        help=(
            "Copy message.proto and write server_skeleton.py + client_skeleton.py "
            "that load the custom interface at startup via compile_and_register()"
        ),
    )

    return parser


def main() -> None:
    """Parse CLI arguments and dispatch to the requested generation action(s)."""
    parser = _build_parser()
    args = parser.parse_args()

    # If no flag was given, show help and exit cleanly.
    if not any(vars(args).values()):
        parser.print_help()
        sys.exit(0)

    output_dir = Path.cwd()
    print(f"grpchook — writing files to: {output_dir}\n")

    # --generate expands to --generate-skeletons + --generate-how-to
    if args.generate:
        generate_skeleton(output_dir)
        generate_how_to(output_dir)
        return

    # Individual flags — execute each that was requested.
    # --generate-skeletons expands to --generate-server + --generate-client.
    if args.generate_skeletons:
        generate_skeleton(output_dir)

    if args.generate_server:
        generate_server(output_dir)

    if args.generate_client:
        generate_client(output_dir)

    if args.generate_how_to:
        generate_how_to(output_dir)

    if args.generate_interface:
        generate_interface(output_dir)

    if args.generate_interface_with_skeletons:
        generate_interface_with_skeletons(output_dir)


if __name__ == "__main__":
    main()
